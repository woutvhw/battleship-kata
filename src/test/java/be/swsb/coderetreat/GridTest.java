    private void testBoatPlacement(String expectedField, Boat... boats) {
        try {
            Grid grid = new Grid();
            for (Boat boat : boats) {
                grid.placeBoat(boat);
            }
            String actualField = grid.render();

            assertEquals(expectedField, actualField);
        } catch (Exception e) {
            fail(e);
        }
    }


